DEVSECOPS_COMMENT_MARKER = "<!-- devsecops-ai-agent-review -->"

def list_issue_comments(owner, repo, issue_number):
    url = f"https://api.github.com/repos/{owner}/{repo}/issues/{issue_number}/comments"

    res = requests.get(
        url,
        headers=github_headers()
    )
    res.raise_for_status()
    return res.json()


def update_issue_comment(owner, repo, comment_id, body):
    url = f"https://api.github.com/repos/{owner}/{repo}/issues/comments/{comment_id}"

    res = requests.patch(
        url,
        headers=github_headers(),
        json={
            "body": body
        }
    )
    res.raise_for_status()
    return res.json()


def post_pr_comment(owner, repo, pr_number, body):
    """
    Create or update a single Security Review Workflow comment on a PR.
    This intentionally overrides the earlier post_pr_comment definition.
    """

    if DEVSECOPS_COMMENT_MARKER not in body:
        body = DEVSECOPS_COMMENT_MARKER + "\n" + body

    comments = list_issue_comments(owner, repo, pr_number)

    for comment in reversed(comments):
        comment_body = comment.get("body") or ""

        if (
            DEVSECOPS_COMMENT_MARKER in comment_body
            or "This comment was generated by Security Review Workflow" in comment_body
            or "Security Review Report" in comment_body
        ):
            result = update_issue_comment(
                owner,
                repo,
                comment["id"],
                body
            )

            safe_record_history("review_comment_updated", {
                "owner": owner,
                "repo": repo,
                "pr_number": pr_number,
                "comment_url": result.get("html_url"),
                "comment_id": result.get("id"),
            })

            return result

    url = f"https://api.github.com/repos/{owner}/{repo}/issues/{pr_number}/comments"

    res = requests.post(
        url,
        headers=github_headers(),
        json={
            "body": body
        }
    )
    res.raise_for_status()
    return res.json()


